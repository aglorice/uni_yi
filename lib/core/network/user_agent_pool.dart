import 'dart:math';

import 'package:shared_preferences/shared_preferences.dart';

/// Manages a User-Agent string for all outgoing HTTP requests.
///
/// On first launch a realistic UA is randomly selected from a pool and
/// persisted via [SharedPreferences]. Subsequent launches reuse the same
/// value so every installation has a unique yet stable fingerprint.
class UserAgentPool {
  UserAgentPool(this._prefs);

  static const _prefKey = 'network.user_agent';

  final SharedPreferences _prefs;
  final Random _random = Random();

  String? _cached;

  /// Returns the UA for this installation, picking one at random on first call.
  String get() {
    return _cached ??= _prefs.getString(_prefKey) ?? _pickAndPersist();
  }

  String _pickAndPersist() {
    final ua = _pool[_random.nextInt(_pool.length)];
    _prefs.setString(_prefKey, ua);
    return ua;
  }

