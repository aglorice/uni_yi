/// Layout breakpoint constants for responsive UI.
class AppBreakpoints {
  AppBreakpoints._();

  /// Width at which [NavigationRail] replaces bottom [NavigationBar].
  static const double rail = 600;

  /// Width at which the desktop layout activates (extended rail + constrained body).
  static const double desktop = 900;

  /// Maximum content width for the constrained body on desktop.
  static const double maxContentWidth = 900;
}
